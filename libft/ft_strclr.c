#include "libft.h"

void	ft_strclr(char *s)
{
	size_t x;

	x = 0;
	if (s == NULL)
		return ;
	while (s[x] != '\0')
	{
		(s[x] = '\0');
		x++;
	}
	s[x] = '\0';
}
