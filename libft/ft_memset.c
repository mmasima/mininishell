#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	x;
	char	*ptr;

	ptr = b;
	x = 0;
	while (x < len)
	{
		ptr[x] = c;
		x++;
	}
	return (b);
}
